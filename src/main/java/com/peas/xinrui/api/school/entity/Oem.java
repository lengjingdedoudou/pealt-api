package com.peas.xinrui.api.school.entity;

public class Oem {

    private String logoMobile;
    private String logoPC;
    private String logoAdm;
    private String logoSAdm;
    private String signature;

    public String getLogoMobile() {
        return logoMobile;
    }

    public void setLogoMobile(String logoMobile) {
        this.logoMobile = logoMobile;
    }

    public String getLogoPC() {
        return logoPC;
    }

    public void setLogoPC(String logoPC) {
        this.logoPC = logoPC;
    }

    public String getLogoAdm() {
        return logoAdm;
    }

    public void setLogoAdm(String logoAdm) {
        this.logoAdm = logoAdm;
    }

    public String getLogoSAdm() {
        return logoSAdm;
    }

    public void setLogoSAdm(String logoSAdm) {
        this.logoSAdm = logoSAdm;
    }

    public String getSignature() {
        return signature;
    }

    public void setSignature(String signature) {
        this.signature = signature;
    }

}
